    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        Tripulante tripulante = new Tripulante();
        tripulante.setNome(txNome.getText());
        tripulante.setDataNascimento(ftxNascimento.getText());
        tripulante.setTelefone(ftxTelefone.getText());
        tripulante.setEndereco(taEndereco.getText());
        tripulante.setSexo(rbMasculino.isSelected() ? 'M' : 'F');
        
        FileHandler<Tripulante> fileHandler = new FileHandler();
        
        File root = new File("./data/tripulantes");
        FileSystemView fsv = new SingleRootFileSystemView(root); 
        JFileChooser fileChooser = new JFileChooser(root, fsv);
        fileChooser.getActionMap().get("New Folder").setEnabled(false);
        fileChooser.setDialogTitle("Salvar como:");
        
        int userSelection = fileChooser.showSaveDialog(this);
        
        if (userSelection == JFileChooser.APPROVE_OPTION) {
            File fileToSave = fileChooser.getSelectedFile();
            
            try {
                fileHandler.save(fileToSave.getAbsolutePath(), tripulante);
            } catch (IOException ex) {
                Logger.getLogger(Home.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            this.setVisible(false);
        }
        
    }//GEN-LAST:event_btnCadastrarActionPerformed

