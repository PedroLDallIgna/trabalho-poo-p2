    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        Submarino submarino = new Submarino();
        submarino.setNome(txNome.getText());
        submarino.setAno(Integer.parseInt(ftxAno.getText()));
        submarino.setCapacidade(Integer.parseInt(ftxCapacidade.getText()));
        Capitao capitao = new Capitao();
        capitao.setNome(txCapitao.getText());
        submarino.setCapitao(capitao);
        
        FileHandler<Submarino> fileHandler = new FileHandler();
        
        File root = new File("./data/embarcacoes");
        FileSystemView fsv = new SingleRootFileSystemView(root); 
        JFileChooser fileChooser = new JFileChooser(root, fsv);
        fileChooser.getActionMap().get("New Folder").setEnabled(false);
        fileChooser.setDialogTitle("Salvar como:");
        
        int userSelection = fileChooser.showSaveDialog(this);
        
        if (userSelection == JFileChooser.APPROVE_OPTION) {
            File fileToSave = fileChooser.getSelectedFile();
            
            try {
                fileHandler.save(fileToSave.getAbsolutePath(), submarino);
            } catch (IOException ex) {
                Logger.getLogger(Home.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            this.setVisible(false);
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

